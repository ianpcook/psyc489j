## ------------------------------------------------------------------------
my.sd <- function(sample.data) {
  N <- length(sample.data)
  sample.mean <- 0
  for (i in 1:N) {
    sample.mean <- sample.mean + sample.data[i]/N
  }
  sq.res <- 0
  for (i in 1:N) {
    sq.res <- sq.res + (sample.data[i] - sample.mean)^2
  }
  variance <- sq.res/(N-1)
  return(sqrt(variance))
}


